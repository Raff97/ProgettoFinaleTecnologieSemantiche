
package com.amongart.semantic.name;

public class ResponseNameItem {

	private String idWikidataItem;
	private String idWikidataAuthor;
	private String urlItem;
    private String nameItem;
    private String urlCreator;
    private String nameCreator;
    
    public String getUrlItem() {
		return urlItem;
	}
	public void setUrlItem(String urlItem) {
		this.urlItem = urlItem;
	}
	public String getNameItem() {
		return nameItem;
	}
	public void setNameItem(String nameItem) {
		this.nameItem = nameItem;
	}
	public String getUrlCreator() {
		return urlCreator;
	}
	public void setUrlCreator(String urlCreator) {
		this.urlCreator = urlCreator;
	}
	public String getNameCreator() {
		return nameCreator;
	}
	public void setNameCreator(String nameCreator) {
		this.nameCreator = nameCreator;
	}
	public String getIdWikidataItem() {
		return idWikidataItem;
	}
	public void setIdWikidataItem(String idWikidataItem) {
		this.idWikidataItem = idWikidataItem;
	}
	public String getIdWikidataAuthor() {
		return idWikidataAuthor;
	}
	public void setIdWikidataAuthor(String idWikidataAuthor) {
		this.idWikidataAuthor = idWikidataAuthor;
	}
	

}
