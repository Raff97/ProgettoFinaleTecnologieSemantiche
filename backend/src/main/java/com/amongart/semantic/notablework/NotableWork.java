
package com.amongart.semantic.notablework;

public class NotableWork {

    private String urlWork;
    private String idWikidataWork;
    private String nameWork;
    private String imageWork;
    private String dateWork;
    
	public String getNameWork() {
		return nameWork;
	}
	public void setNameWork(String nameWork) {
		this.nameWork = nameWork;
	}
	public String getUrlWork() {
		return urlWork;
	}
	public void setUrlWork(String urlWork) {
		this.urlWork = urlWork;
	}
	public String getImageWork() {
		return imageWork;
	}
	public void setImageWork(String imageWork) {
		this.imageWork = imageWork;
	}
	public String getDateWork() {
		return dateWork;
	}
	public void setDateWork(String dateWork) {
		this.dateWork = dateWork;
	}
	public String getIdWikidataWork() {
		return idWikidataWork;
	}
	public void setIdWikidataWork(String idWikidataWork) {
		this.idWikidataWork = idWikidataWork;
	}
    

}
